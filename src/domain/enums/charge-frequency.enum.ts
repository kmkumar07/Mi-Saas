/**
 * Charge frequency enum
 * Defines how often a charge is applied to a subscription
 */
export enum ChargeFrequency {
    ONE_TIME = 'one-time',
    MONTHLY = 'monthly',
    FORTNIGHTLY = 'fortnightly',
    WEEKLY = 'weekly',
    DAILY = 'daily',
    HOURLY = 'hourly',
    PER_MINUTE = 'per-minute',
    PER_SECOND = 'per-second',
}
